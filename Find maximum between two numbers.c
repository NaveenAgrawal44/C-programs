// C program to find maximum between two numbers

#include<stdio.h>
int main()
{
   int x,y;
   printf("Enter first number x=");
   scanf("%d",&x);
   printf("Enter second number y=");
   scanf("%d",&y);
   if(x>y)
   {
     printf("x is greater");
   }
   else
   {
     printf("y is greater");
   }
   return 0;
}